package application;

public class Indice {

	private int indice;
	private String targa;

	public int getIndice() {
		return indice;
	}

	public void setIndice(int indice) {
		this.indice = indice;
	}

	public String getTarga() {
		return targa;
	}

	public void setTarga(String targa) {
		this.targa = targa;
	}

	public Indice(int indice, String targa) {
		this.indice = indice;
		this.targa = targa;
	}

}
